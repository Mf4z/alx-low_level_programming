#include "3-calc.h"

/**
  * op_add - function that calculates sum of two integers
  *
  * @a: First operand
  * @b: Second operand
  *
  * Return: Returns the sum of a and b
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - function that calculates difference of two integers
  *
  * @a: First operand
  * @b: Second operand
  *
  * Return: Returns the difference of a and b
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - function that calculates product of two integers
  *
  * @a: First operand
  * @b: Second operand
  *
  * Return: Returns the product of a and b
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - function that calculates quotioent of two integers
  *
  * @a: First operand
  * @b: Second operand
  *
  * Return: Returns the quotient of a and b
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - function that calculates remainder of two integers
  *
  * @a: First operand
  * @b: Second operand
  *
  * Return: Returns the remainder of a and b
  */
int op_mod(int a, int b)
{
	return (a % b);
}
